import { spawnSync } from "node:child_process";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const manifestPath = resolve(
	rootDir,
	"packages/contracts/src/headless-protocol.manifest.json",
);
const tsOutputPath = resolve(
	rootDir,
	"packages/contracts/src/headless-protocol-generated.ts",
);
const rustOutputPath = resolve(
	rootDir,
	"packages/tui-rs/src/headless/generated_protocol.rs",
);

function toTsConst(values) {
	if (values.length <= 2) {
		return `[${values.map((value) => JSON.stringify(value)).join(", ")}] as const`;
	}
	return `[\n${values
		.map((value) => `\t${JSON.stringify(value)},`)
		.join("\n")}\n] as const`;
}

function toRustArray(values) {
	return `&[${values.map((value) => JSON.stringify(value)).join(", ")}]`;
}

function renderTs(manifest) {
	return `/**
 * Auto-generated by \`scripts/headless-protocol-codegen.mjs\`.
 * Do not edit manually; update \`headless-protocol.manifest.json\` instead.
 */

export const headlessProtocolVersion = ${JSON.stringify(manifest.protocolVersion)} as const;

export const headlessServerRequestTypes = ${toTsConst(manifest.serverRequestTypes)};
export type HeadlessServerRequestType =
\t(typeof headlessServerRequestTypes)[number];

export const headlessServerRequestResolutions = ${toTsConst(
		manifest.serverRequestResolutions,
	)};
export type HeadlessServerRequestResolution =
\t(typeof headlessServerRequestResolutions)[number];

export const headlessServerRequestResolvedBy = ${toTsConst(
		manifest.serverRequestResolvedBy,
	)};
export type HeadlessServerRequestResolvedBy =
\t(typeof headlessServerRequestResolvedBy)[number];

export const headlessConnectionRoles = ${toTsConst(manifest.connectionRoles)};
export type HeadlessConnectionRole = (typeof headlessConnectionRoles)[number];

export const headlessThinkingLevels = ${toTsConst(manifest.thinkingLevels)};
export type HeadlessThinkingLevel = (typeof headlessThinkingLevels)[number];

export const headlessApprovalModes = ${toTsConst(manifest.approvalModes)};
export type HeadlessApprovalMode = (typeof headlessApprovalModes)[number];

export const headlessErrorTypes = ${toTsConst(manifest.errorTypes)};
export type HeadlessErrorType = (typeof headlessErrorTypes)[number];

export const headlessUtilityOperations = ${toTsConst(
		manifest.utilityOperations,
	)};
export type HeadlessUtilityOperation =
\t(typeof headlessUtilityOperations)[number];

export const headlessUtilityCommandStreams = ${toTsConst(
		manifest.utilityCommandStreams,
	)};
export type HeadlessUtilityCommandStream =
\t(typeof headlessUtilityCommandStreams)[number];

export const headlessUtilityCommandShellModes = ${toTsConst(
		manifest.utilityCommandShellModes,
	)};
export type HeadlessUtilityCommandShellMode =
\t(typeof headlessUtilityCommandShellModes)[number];
`;
}

function formatTs(source) {
	const result = spawnSync(
		"bunx",
		["biome", "format", "--stdin-file-path", tsOutputPath],
		{
			cwd: rootDir,
			encoding: "utf8",
			input: source,
		},
	);
	if (result.status !== 0) {
		throw new Error(
			`Failed to format generated TypeScript protocol file: ${result.stderr || result.stdout}`,
		);
	}
	return result.stdout;
}

function renderRust(manifest) {
	return `#![allow(dead_code)]

// Auto-generated by \`scripts/headless-protocol-codegen.mjs\`.
// Do not edit manually; update \`headless-protocol.manifest.json\` instead.

pub const HEADLESS_PROTOCOL_VERSION: &str = ${JSON.stringify(
		manifest.protocolVersion,
	)};
pub const HEADLESS_SERVER_REQUEST_TYPES: &[&str] = ${toRustArray(
		manifest.serverRequestTypes,
	)};
pub const HEADLESS_SERVER_REQUEST_RESOLUTIONS: &[&str] = ${toRustArray(
		manifest.serverRequestResolutions,
	)};
pub const HEADLESS_SERVER_REQUEST_RESOLVED_BY: &[&str] = ${toRustArray(
		manifest.serverRequestResolvedBy,
	)};
pub const HEADLESS_CONNECTION_ROLES: &[&str] = ${toRustArray(
		manifest.connectionRoles,
	)};
pub const HEADLESS_THINKING_LEVELS: &[&str] = ${toRustArray(
		manifest.thinkingLevels,
	)};
pub const HEADLESS_APPROVAL_MODES: &[&str] = ${toRustArray(
		manifest.approvalModes,
	)};
pub const HEADLESS_ERROR_TYPES: &[&str] = ${toRustArray(manifest.errorTypes)};
pub const HEADLESS_UTILITY_OPERATIONS: &[&str] = ${toRustArray(
		manifest.utilityOperations,
	)};
pub const HEADLESS_UTILITY_COMMAND_STREAMS: &[&str] = ${toRustArray(
		manifest.utilityCommandStreams,
	)};
pub const HEADLESS_UTILITY_COMMAND_SHELL_MODES: &[&str] = ${toRustArray(
		manifest.utilityCommandShellModes,
	)};
`;
}

function formatRust(source) {
	const result = spawnSync("rustfmt", ["--emit", "stdout"], {
		cwd: rootDir,
		encoding: "utf8",
		input: source,
	});
	if (result.status !== 0) {
		throw new Error(
			`Failed to format generated Rust protocol file: ${result.stderr || result.stdout}`,
		);
	}
	return result.stdout;
}

async function main() {
	const check = process.argv.includes("--check");
	const manifest = JSON.parse(await readFile(manifestPath, "utf8"));
	const targets = [
		{
			path: tsOutputPath,
			content: formatTs(renderTs(manifest)),
		},
		{
			path: rustOutputPath,
			content: formatRust(renderRust(manifest)),
		},
	];

	if (check) {
		let failed = false;
		for (const target of targets) {
			const current = await readFile(target.path, "utf8").catch(() => null);
			if (current !== target.content) {
				failed = true;
				console.error(
					`Headless protocol generated file is out of date: ${target.path}`,
				);
			}
		}
		if (failed) {
			process.exitCode = 1;
		}
		return;
	}

	for (const target of targets) {
		await mkdir(dirname(target.path), { recursive: true });
		await writeFile(target.path, target.content);
	}
}

await main();
