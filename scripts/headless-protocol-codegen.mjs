function toTsConst(values) {
	if (values.length <= 2) {
		return `[${values.map((value) => JSON.stringify(value)).join(", ")}] as const`;
	}
	return `[\n${values
		.map((value) => `\t${JSON.stringify(value)},`)
		.join("\n")}\n] as const`;
}

function toRustArray(values) {
	return `&[${values.map((value) => JSON.stringify(value)).join(", ")}]`;
}

