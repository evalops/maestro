function toTsConst(values) {
	if (values.length <= 2) {
		return `[${values.map((value) => JSON.stringify(value)).join(", ")}] as const`;
	}
	return `[\n${values
		.map((value) => `\t${JSON.stringify(value)},`)
		.join("\n")}\n] as const`;
}

function toRustArray(values) {
	return `&[${values.map((value) => JSON.stringify(value)).join(", ")}]`;
}

function toEnumPrefix(enumName) {
	return enumName
		.replace(/([a-z0-9])([A-Z])/g, "$1_$2")
		.toUpperCase();
}

function parseProtoVersion(source) {
	const match = source.match(/protocol-version:\s*([0-9-]+)/);
	if (!match) {
		throw new Error(
			"Missing protocol-version comment in proto/maestro/v1/headless.proto",
		);
	}
	return match[1];
}

function parseProtoEnums(source) {
	const enums = new Map();
	const enumPattern = /enum\s+(\w+)\s*\{([\s\S]*?)\n\}/g;
	for (const match of source.matchAll(enumPattern)) {
		const [, enumName, body] = match;
		const prefix = `${toEnumPrefix(enumName)}_`;
		const values = [];
		for (const line of body.split("\n")) {
			const trimmed = line.trim();
			if (!trimmed || trimmed.startsWith("//")) {
				continue;
			}
			const valueMatch = trimmed.match(/^([A-Z0-9_]+)\s*=\s*\d+;/);
			if (!valueMatch) {
				continue;
			}
			const identifier = valueMatch[1];
			if (identifier.endsWith("_UNSPECIFIED")) {
				continue;
			}
			if (!identifier.startsWith(prefix)) {
				throw new Error(`Enum value ${identifier} does not match prefix ${prefix}`);
			}
			values.push(identifier.slice(prefix.length).toLowerCase());
		}
		enums.set(enumName, values);
	}
	return enums;
}

function parseOneofFields(source, messageName) {
	const pattern = new RegExp(
		`message\\s+${messageName}\\s*\\{[\\s\\S]*?oneof\\s+payload\\s*\\{([\\s\\S]*?)\\n\\s*\\}\\n\\}`,
	);
	const match = source.match(pattern);
	if (!match) {
		throw new Error(`Missing oneof payload in ${messageName}`);
	}
	const values = [];
	for (const line of match[1].split("\n")) {
		const trimmed = line.trim();
		if (!trimmed || trimmed.startsWith("//")) {
			continue;
		}
		const fieldMatch = trimmed.match(/^\w+\s+(\w+)\s*=\s*\d+;/);
		if (!fieldMatch) {
			continue;
		}
		values.push(fieldMatch[1]);
	}
	return values;
}

function buildProtocolSpec(source) {
	const protoEnums = parseProtoEnums(source);
	const protocolSpec = {
		protocolVersion: parseProtoVersion(source),
		toAgentMessageTypes: parseOneofFields(source, "ToAgentEnvelope"),
		fromAgentMessageTypes: parseOneofFields(source, "FromAgentEnvelope"),
	};

	for (const [enumName, exportKey] of Object.entries(protoToExportKey)) {
		const values = protoEnums.get(enumName);
		if (!values) {
			throw new Error(`Missing enum ${enumName} in proto/maestro/v1/headless.proto`);
		}
		protocolSpec[exportKey] = values;
	}

	return protocolSpec;
}

