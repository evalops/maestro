/// Computed session statistics (not serialized to JSONL).
///
/// Aggregates metrics across all messages in a session for display purposes.
/// These values are calculated during session reading and are not stored in
/// the session file itself.
///
/// # Usage
///
/// ```rust,ignore
/// let session = SessionReader::read_file(path)?;
/// println!("Total messages: {}", session.stats.total_messages());
/// println!("Token usage: {} in, {} out",
///     session.stats.total_input_tokens,
///     session.stats.total_output_tokens);
/// println!("Total cost: ${:.4}", session.stats.total_cost);
/// ```
#[derive(Debug, Clone, Default)]
pub struct SessionStats {
    /// Count of user messages in the session.
    pub user_messages: usize,

    /// Count of assistant messages in the session.
    pub assistant_messages: usize,

    /// Count of tool call requests made by the assistant.
    pub tool_calls: usize,

    /// Count of tool result messages sent back to the assistant.
    pub tool_results: usize,

    /// Sum of all input tokens across assistant messages.
    pub total_input_tokens: u64,

    /// Sum of all output tokens across assistant messages.
    pub total_output_tokens: u64,

    /// Sum of all costs across assistant messages (in USD).
    pub total_cost: f64,
}

impl SessionStats {
    pub fn total_messages(&self) -> usize {
        self.user_messages + self.assistant_messages + self.tool_results
    }
}

