/// Conversation message with role-based discrimination.
///
/// Messages are serialized with a `"role"` field that determines the variant, matching
/// the format used by AI provider APIs (Anthropic, OpenAI, etc.).
///
/// # Role Serialization
///
/// The `#[serde(tag = "role")]` attribute creates JSON like:
/// ```json
/// {"role":"user","content":"Hello",...}
/// {"role":"assistant","content":[{"type":"text","text":"Hi"}],...}
/// {"role":"toolResult","tool_call_id":"call_123",...}
/// ```
#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(tag = "role", rename_all = "camelCase")]
pub enum AppMessage {
    /// User-authored message (human input).
    ///
    /// Can contain plain text or structured content blocks (text + images).
    User {
        /// Message content (either a simple string or structured blocks).
        content: MessageContent,

        /// Optional file attachments.
        #[serde(skip_serializing_if = "Option::is_none")]
        attachments: Option<Vec<Attachment>>,

        /// Unix timestamp in milliseconds.
        ///
        /// Defaults to 0 if missing during deserialization.
        #[serde(default)]
        timestamp: u64,
    },

    /// Assistant-generated response.
    ///
    /// Contains structured content blocks (text, thinking, tool calls) along with
    /// token usage statistics and metadata.
    Assistant {
        /// Response content as a list of structured blocks.
        ///
        /// Can contain text, thinking, tool calls, or images.
        content: Vec<ContentBlock>,

        /// API name (e.g., "anthropic", "openai").
        ///
        /// Omitted if None.
        #[serde(skip_serializing_if = "Option::is_none")]
        api: Option<String>,

        /// Provider name (e.g., "Anthropic", "OpenAI").
        ///
        /// Omitted if None.
        #[serde(skip_serializing_if = "Option::is_none")]
        provider: Option<String>,

        /// Model identifier that generated this response.
        ///
        /// May differ from session model if the user switched models mid-session.
        /// Omitted if None.
        #[serde(skip_serializing_if = "Option::is_none")]
        model: Option<String>,

        /// Token consumption statistics.
        ///
        /// Includes input/output tokens, cache usage, and cost breakdown.
        /// Omitted if None.
        #[serde(skip_serializing_if = "Option::is_none")]
        usage: Option<TokenUsage>,

        /// Reason the model stopped generating.
        ///
        /// Common values: "end_turn", "max_tokens", "stop_sequence", "tool_use".
        /// Omitted if None.
        #[serde(skip_serializing_if = "Option::is_none")]
        stop_reason: Option<String>,

        /// Unix timestamp in milliseconds.
        ///
        /// Defaults to 0 if missing.
        #[serde(default)]
        timestamp: u64,
    },

    /// Tool execution result.
    ///
    /// Sent back to the assistant after executing a tool call to provide the output.
    ToolResult {
        /// Unique identifier linking this result to the tool call.
        ///
        /// Matches the `id` field from the `ContentBlock::ToolCall`.
        tool_call_id: String,

        /// Name of the tool that was executed.
        tool_name: String,

        /// Tool output (success result or error message).
        content: String,

        /// Structured tool execution details.
        ///
        /// Can include timing, file paths, or other metadata. Omitted if None.
        #[serde(skip_serializing_if = "Option::is_none")]
        details: Option<serde_json::Value>,

        /// Whether this result represents an error.
        ///
        /// Defaults to false if missing.
        #[serde(default)]
        is_error: bool,

        /// Unix timestamp in milliseconds.
        ///
        /// Defaults to 0 if missing.
        #[serde(default)]
        timestamp: u64,
    },
}

impl AppMessage {
    /// Get the role of this message
    pub fn role(&self) -> &'static str {
        match self {
            AppMessage::User { .. } => "user",
            AppMessage::Assistant { .. } => "assistant",
            AppMessage::ToolResult { .. } => "toolResult",
        }
    }

    /// Get the timestamp
    pub fn timestamp(&self) -> u64 {
        match self {
            AppMessage::User { timestamp, .. } => *timestamp,
            AppMessage::Assistant { timestamp, .. } => *timestamp,
            AppMessage::ToolResult { timestamp, .. } => *timestamp,
        }
    }

    /// Get text content (for display)
    pub fn text_content(&self) -> String {
        match self {
            AppMessage::User { content, .. } => match content {
                MessageContent::Text(s) => s.clone(),
                MessageContent::Blocks(blocks) => blocks
                    .iter()
                    .filter_map(|b| {
                        if let ContentBlock::Text { text } = b {
                            Some(text.as_str())
                        } else {
                            None
                        }
                    })
                    .collect::<Vec<_>>()
                    .join(""),
            },
            AppMessage::Assistant { content, .. } => content
                .iter()
                .filter_map(|b| {
                    if let ContentBlock::Text { text } = b {
                        Some(text.as_str())
                    } else {
                        None
                    }
                })
                .collect::<Vec<_>>()
                .join(""),
            AppMessage::ToolResult { content, .. } => content.clone(),
        }
    }
}

