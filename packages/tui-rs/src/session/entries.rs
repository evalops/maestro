/// Token consumption and cost tracking for assistant messages.
///
/// Tracks both token counts and associated costs, with special handling for
/// prompt caching which provides discounted rates for repeated context.
///
/// # Prompt Caching
///
/// - `cache_write`: Tokens written to cache on first use (full price)
/// - `cache_read`: Tokens read from cache on subsequent uses (90% discount)
///
/// Example: A 10,000 token system prompt costs full price the first time, but
/// only 10% of that price on cache hits.
///
/// # Field Naming
///
/// Uses camelCase for JSON compatibility with the TypeScript codebase:
/// - `cacheRead` instead of `cache_read`
/// - `cacheWrite` instead of `cache_write`
#[derive(Debug, Clone, Default, Serialize, Deserialize)]
pub struct TokenUsage {
    /// Tokens sent to the model (prompt + user message).
    ///
    /// Defaults to 0 if missing.
    #[serde(default)]
    pub input: u64,

    /// Tokens generated by the model (response text).
    ///
    /// Defaults to 0 if missing.
    #[serde(default)]
    pub output: u64,

    /// Tokens served from prompt cache (discounted cost).
    ///
    /// Defaults to 0 if missing.
    #[serde(default, rename = "cacheRead")]
    pub cache_read: u64,

    /// Tokens written to prompt cache (full cost).
    ///
    /// Defaults to 0 if missing.
    #[serde(default, rename = "cacheWrite")]
    pub cache_write: u64,

    /// Detailed cost breakdown by token type.
    ///
    /// Omitted if None (older sessions may not have cost data).
    #[serde(skip_serializing_if = "Option::is_none")]
    pub cost: Option<TokenCost>,
}

impl TokenUsage {
    pub fn total(&self) -> u64 {
        self.input + self.output
    }

    pub fn total_cost(&self) -> f64 {
        self.cost.as_ref().map(|c| c.total).unwrap_or(0.0)
    }
}

/// Token cost breakdown
#[derive(Debug, Clone, Default, Serialize, Deserialize)]
pub struct TokenCost {
    #[serde(default)]
    pub input: f64,
    #[serde(default)]
    pub output: f64,
    #[serde(default, rename = "cacheRead")]
    pub cache_read: f64,
    #[serde(default, rename = "cacheWrite")]
    pub cache_write: f64,
    #[serde(default)]
    pub total: f64,
}

